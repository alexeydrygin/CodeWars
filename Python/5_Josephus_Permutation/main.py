def josephus(items, k):
    # инициализируем список для хранения результата
    result = []
    # инициализируем переменную для отслеживания индекса элемента, который нужно удалить
    index = 0
    # итерируем, пока все элементы не будут удалены из исходного списка
    while len(items) > 0:
        # увеличиваем индекс на k-1 (так как мы начинаем считать с 0)
        index = (index + k - 1) % len(items)
        # удаляем элемент с текущим индексом и добавляем его в список результата
        result.append(items.pop(index))
    # возвращаем список результата
    return result
